                    <h1>What is asthma?</h1>
                    <p>Asthma is a long-term lung condition.  People with asthma have sensitive airways in their lungs which react to triggers, causing a ‘flare-up’. In a flare-up, the muscles around the airway squeeze tight, the airways swell and become narrow and there is more mucus. These things make it harder to breathe.</p>
                    <p>An asthma flare-up can come on slowly (over hours, days or even weeks) or very quickly (over minutes). A sudden or severe asthma flare-up is sometimes called an asthma attack.</p>
                    <p>One in ten people in Australia has asthma. It affects people of all ages. Some people get asthma when they are young; others when they are older.</p>
                    <p>Asthma cannot be cured, but for most people it can be well controlled by following a daily management plan.</p>
                    <h3>What are the symptoms of asthma?</h3>
                    <p>A person’s asthma symptoms can vary over time - sometimes they will have no symptoms, especially when their asthma is well-controlled. Symptoms often vary from person to person, but they are most commonly:</p>
                    <ul>
                        <li>breathlessness</li>
                        <li>wheezing</li>
                        <li>tight feeling in the chest</li>
                        <li>continuing cough</li>
                    </ul>
                    <p>Symptoms often occur at night, early in the morning or during/just after activity. They are caused by the narrowing of the airways.</p>
                    <p>If your asthma is well controlled, you should only have occasional asthma symptoms.</p>
                    <p>If you have symptoms regularly, you should see your doctor, and visit the <a href="http://www.asthmaaustralia.org.au/national/about-asthma/manage-your-asthma/controlling-asthma/controlling-asthma" target="_blank">Controlling Asthma</a> section.</p>
                    <p>If you’re not sure if you have asthma you should see your doctor. There is more information on diagnosis in <a href="http://www.asthmaaustralia.org.au/National/About-Asthma/Could-it-be-asthma-/could-it-be-asthma" target="_blank">Could it be asthma?</a></p>
                    <h3>Causes</h3>
                    <p>The causes of asthma are not fully understood, although people with asthma often have a family history of asthma, eczema and hayfever.</p>
                    <p>Research has shown that exposure to tobacco smoke (especially as a baby or young child), obesity and some workplace chemicals can increase the risk of developing asthma.</p>
                    <p>Researchers continue to try to find out more about what causes asthma and how we might prevent it.</p>
                    <h1>Manage your asthma</h1>
                    <p>Managing your asthma means taking control of your health so that you can live a full and active life. While asthma can’t be cured, for most people it can be well controlled, which means hardly any symptoms or flare-ups, or limits to your lifestyle. With the correct knowledge, skills and medication you can do just about anything - asthma shouldn't stop you! </p>
                    <p><b>Your asthma medicine</b> - The key way asthma is managed is with inhaled medicines – make sure you know what your medicine does and why you are taking it. <a href="http://www.asthmaaustralia.org.au/national/about-asthma/manage-your-asthma/your-asthma-medicine/your-asthma-medicine" target="_blank">Read more...</a></p>
                    <p><b>How to use your puffer</b> - The way you take your medicine is the most important thing, otherwise you won’t be getting the right dose in the right place. <a href="http://www.asthmaaustralia.org.au/national/about-asthma/manage-your-asthma/how-to-use-your-puffer/how-to-use-your-puffer" target="_blank">Read more...</a></p>
                    <p><b>Controlling asthma</b> - What is normal? Find out if your asthma is under control or if you can take steps to improve it. <a href="http://www.asthmaaustralia.org.au/national/about-asthma/manage-your-asthma/controlling-asthma/controlling-asthma" target="_blank">Read more...</a></p>
                    <p><b>Common concerns</b> - Including side effects, worries about steroids, asthma in pregnancy and anaphylaxis. <a href="http://www.asthmaaustralia.org.au/National/About-Asthma/Manage-your-asthma/Common-concerns/common-concerns" target="_blank">Read more...</a></p>
