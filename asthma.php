                    <h1>Asthma emergency</h1>
                    <h3>1 Sit the person upright</h3>
                    <ul>
                        <li>Be calm and reassuring</li>
                        <li>Do not leave them alone</li>
                    </ul>
                    <h3>2 Give 4 separate puffs of blue/grey reliever puffer</h3>
                    <ul>
                        <li>Shake puffer</li>
                        <li>Put 1 puff into spacer</li>
                        <li>Take 4 breaths from spacer</li>
                    </ul>
                    <p>Repeat until 4 puffs have been taken</p>
                    <p>Remember: Shake, 1 puff, 4 breaths</p>
                    <h3>3 Wait 4 minutes</h3>
                    <ul>
                        <li>if there is no improvement, give 4 more separate puffs of blue/grey reliever as above</li>
                    </ul>
                    <h3 style="color: red">4 If there is still no improvement call emergency assistance (DIAL 000)</h3>
                    <ul  style="color: red">
                        <li>Say ‘ambulance’ and that someone is having an asthma attack</li>
                        <li>Keep giving 4 separate puffs every 4 minutes until emergency assistance arrives</li>
                    </ul>
